package MenuLib;

public class Dish {

    public Dish(String name, float coast, int calories, boolean veganFriendly) {
        this.Name = name;
        this.Coast = coast;
        this.Calories = calories;
        this.VeganFriendly = veganFriendly;
    }
    public String Name;
    public float Coast;
    public int Calories;
    public boolean VeganFriendly;
}
